#include <unistd.h>
#include <stdio.h>

int	main(int args, char *argv[])
{
	int i;

	i = 0;
	while (i < args)
	{
		i++;
	}
	i--;
	while (i > 0)
	{
		printf("\n%s", argv[i]);
		i--;
	}
	return (0);
}
